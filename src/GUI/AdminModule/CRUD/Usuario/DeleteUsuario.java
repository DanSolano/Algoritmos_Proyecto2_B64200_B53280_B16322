    private boolean exist(String username, String rol) {
        boolean exist = false;
        for (Usuario usuario : usuarios) {
            if (usuario.getUsuario().equals(username)
                    && usuario.getRol().equals(rol)) {
                exist = true;
            }
        }
        return exist;
    }

